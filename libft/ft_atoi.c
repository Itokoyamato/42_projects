#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int result;
	int is_negative;

	i = 0;
	is_negative = 0;
	result = 0;
	while (ft_isspace(str[i]))
		i++;
	if (str[i] == '-')
	{
		is_negative = 1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (ft_isdigit(str[i]))
	{
		result = result * 10 + (str[i] - 48);
		i++;
	}
	if (is_negative == 1)
		result = result * -1;
	return (result);
}
