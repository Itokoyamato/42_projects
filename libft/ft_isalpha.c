#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned int		uc;

	uc = (unsigned int)c;
	if ((uc < 65 || uc > 90) && (uc < 97 || uc > 122))
	{
		return (0);
	}
	return (1);
}
