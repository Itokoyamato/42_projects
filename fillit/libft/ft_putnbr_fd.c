#include "libft.h"

void	ft_putnbr_fd(int nbr, int fd)
{
	unsigned int nb;

	if (nbr < 0)
	{
		ft_putchar_fd('-', fd);
		nb = nbr * -1;
	}
	else
		nb = nbr;
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
		ft_putchar_fd(nb + '0', fd);
}
