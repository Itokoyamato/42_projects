int		ft_iterative_factorial(int nb)
{
	int i;
	int c;

	i = 1;
	c = 1;
	if (nb < 0 || nb > 12)
		return (0);
	while (i <= nb)
	{
		c = c * i;
		i++;
	}
	nb = c;
	return (nb);
}
