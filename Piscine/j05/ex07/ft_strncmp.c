int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (n-- && *s1 && *s2)
	{
		if (*s1++ != *s2++)
			return (*(s1 - 1) - *(s2 - 1));
		if (n == 0)
			return (0);
	}
	return (*s1 - *s2);
}
