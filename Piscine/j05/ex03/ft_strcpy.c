char	*ft_strcpy(char *dest, const char *src)
{
	int		i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		if (src[i] == '\0')
		{
			while (dest[i] != '\0')
			{
				dest[i] = '\0';
				i++;
			}
			return (dest);
		}
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
